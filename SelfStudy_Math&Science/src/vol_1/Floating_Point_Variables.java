package vol_1;

public class Floating_Point_Variables {

	public static void main(String[] args) {

		// DOUBLE FLOATING VARIABLE - holds numbers with decimals
		// Converting Inches to Centimeter

		int in; // declaration
		double cm;

		in = 12;
		cm = in * 2.54; // value of inch to centimeter

		System.out.println(in + " inch(es) is equal to " + cm + " centimeters.");
		


	}

}
