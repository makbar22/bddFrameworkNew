package vol_1;

public class Lesson_25_Exercise_LogicalOperators {

	public static void main(String[] args) {

		int age = 45;

		if ((age >= 25) && (age <= 35))
			System.out.println("Your age is between 25 and 35.");

		if ((age >= 45) && (age <= 55))
			System.out.println("Your age is between 45 and 55.");

	}
}
