package vol_1;

public class DeclaringAndInitializing {

	public static void main(String[] args) {

		int a; // declaration
		int b;
		int c;

		a = 10; // initialization
		b = 15;
		c = 20;

		// Or

		int d, e, f; // declaration

		d = 10; // initialization
		e = 15;
		f = 20;

		// Or

		int x = 10, y = 15, z = 20; // declaration and initialization

	}
}
